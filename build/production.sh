DATE=`date +%Y-%m-%d:%H:%M:%S`
PACKAGE_VERSION=$(cat package.json \
  | grep version \
  | head -1 \
  | awk -F: '{ print $2 }' \
  | sed 's/[",]//g' \
  | tr -d '[[:space:]]')

git add .
git commit -m "$VERSION $DATE production"

git push origin master

./bump.sh minor

NEW_PACKAGE_VERSION=$(cat package.json \
  | grep version \
  | head -1 \
  | awk -F: '{ print $2 }' \
  | sed 's/[",]//g' \
  | tr -d '[[:space:]]')

git tag "v$NEW_PACKAGE_VERSION"
